    // Metodo para ingresar productos a la tabla
    private void btnanadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnanadirActionPerformed

    try{
        
    int id = contadorID;
    contadorID++; // Incrementa para el siguiente producto (ID)

    //Obtener los valores del textfield (Valores de la tabla)
    String pNombre = etnombre.getText();
    double valor = Double.valueOf(etvalor.getText());
    int pCantidad = Integer.valueOf(etcantidad.getText());

    // Cálculo de la ganancia (Se saca el 10% a cada producto, luego lo sumamos y a ese valor lo vendemos)
    double ganancia = valor * 0.05;
    double total_ganancia = ganancia + valor; 

    // Agregar fila a la tabla
    mt.addRow(new Object[] {id, pNombre, valor, pCantidad, total_ganancia});

    // Limpiar campos
    etnombre.setText("");
    etvalor.setText("");
    etcantidad.setText("");
    
    //Añadir al vector
    Productos nuevoProducto = new Productos(id,pNombre,valor,pCantidad,total_ganancia);
    productos.add(nuevoProducto);
    
    //Ingresar un producto al arreglo global que esta en el principal
    Principal.productos.add(new Productos(id, pNombre, valor, pCantidad, total_ganancia));
     
    //Ingresar un producto al arreglo global que esta en BuscarProductos
    BuscarProductos.productos.add(new Productos(id, pNombre, valor, pCantidad, total_ganancia));
    
    //Mostrar el vector
    for(Productos p : productos){
        System.out.println("id:"+p.getId());
        System.out.println("Nombre:"+p.getNombre());
        System.out.println("Valor Compra"+p.getValorCompra());
        System.out.println("Cantidad:"+p.getCantidad());
        System.out.println("La ganancia es:"+p.getGanancia());
        System.out.println("-------------------------------");
    }
    
    }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese un dato valido por favor");
        }
    }//GEN-LAST:event_btnanadirActionPerformed

