    //Actualizar un campo de la tabla
    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        //Selecciona fila 
        int selectedRow = jTable1.getSelectedRow();

        // Actualizar el nombre solo si no está vacío
        String nombre = etnombre.getText();
        if (!nombre.isEmpty()) {
        mt.setValueAt(nombre, selectedRow, 1);
        }

        // Actualizar el valor solo si no está vacío
        String valorText = etvalor.getText();
        if (!valorText.isEmpty()) {
        double valor = Double.parseDouble(valorText);
        mt.setValueAt(valorText, selectedRow, 2);

        // Cálculo de ganancia (precio + 5%)
        double ganancia = valor + (valor * 0.05);
        mt.setValueAt(ganancia, selectedRow, 4);
        }

        // Actualizar la cantidad solo si no está vacía
        String cantidad = etcantidad.getText();
        if (!cantidad.isEmpty()) {
        mt.setValueAt(cantidad, selectedRow, 3);
      }
    }//GEN-LAST:event_btnactualizarActionPerformed

